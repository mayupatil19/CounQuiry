package com.example.hp.coun.mGridViewData;

public class Category
{
    String name;
    int image;
    public Category()
    {

    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name=name;
    }

    public int getImage()
    {
        return image;
    }
    public void setImage(int image)
    {
        this.image=image;
    }
}